-- ===== 7. RLS 정책 =====
-- user: 연락처(phone/email)는 행 단위로 보호. 현재 정책은 전체 컬럼 SELECT 를
--       본인/공개로 나눠 두 정책으로 운영. (공개 프로필 조회는 닉네임·bio 노출 목적)
drop policy if exists "user: 공개 프로필 조회" on public."user";
create policy "user: 공개 프로필 조회" on public."user"
  for select using (true);

drop policy if exists "user: 본인 조회" on public."user";
create policy "user: 본인 조회" on public."user"
  for select using (auth.uid() = id);

drop policy if exists "user: 본인 수정" on public."user";
create policy "user: 본인 수정" on public."user"
  for update using (auth.uid() = id) with check (auth.uid() = id);

drop policy if exists "user: 트리거 insert 허용" on public."user";
create policy "user: 트리거 insert 허용" on public."user"
  for insert with check (true);

