-- ===== 1. ENUM 타입 =====
do $$ begin
  if not exists (select 1 from pg_type where typname = 'match_status') then
    create type public.match_status as enum ('interested','accepted','declined','completed','failed');
  end if;
  if not exists (select 1 from pg_type where typname = 'post_status') then
    create type public.post_status as enum ('active','closed');
  end if;
  if not exists (select 1 from pg_type where typname = 'post_type') then
    create type public.post_type as enum ('offer','request');
  end if;
end $$;

-- ===== 2. 테이블 =====
-- 사용자 (auth.users 와 1:1, id 동일). "user" 는 예약어라 항상 따옴표 필요.
create table if not exists public."user" (
  id              uuid primary key default gen_random_uuid(),
  created_at      timestamptz not null default now(),
  email           text,
  nickname        text,
  phone           text,
  terms_agreed_at timestamptz,
  bio             text
);

-- 게시글 (있어요 / 구해요)
create table if not exists public.posts (
  id            bigint generated by default as identity primary key,
  author_id     uuid not null references public."user"(id) on delete cascade,
  post_type     public.post_type not null,
  title         text not null,
  body          text not null,
  region        text not null,
  image_url     text,
  status        public.post_status not null default 'active',
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now(),
  contact_email text,
  amount        text,
  timing        text
);

-- 매칭
create table if not exists public.matches (
  id                  bigint generated by default as identity primary key,
  initiator_id        uuid not null references public."user"(id) on delete cascade,
  post_id             bigint not null references public.posts(id) on delete cascade,
  post_author_id      uuid not null references public."user"(id) on delete cascade,
  message             text not null,
  status              public.match_status not null default 'interested',
  contact_revealed_at timestamptz,
  status_history      jsonb not null default '[]'::jsonb,
  created_at          timestamptz not null default now(),
  updated_at          timestamptz not null default now(),
  constraint unique_match unique (initiator_id, post_id),
  constraint no_self_match check (initiator_id <> post_author_id)
);

-- 좋아요
create table if not exists public.likes (
  id         bigint generated always as identity primary key,
  user_id    uuid not null references public."user"(id) on delete cascade,
  post_id    bigint not null references public.posts(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint likes_user_id_post_id_key unique (user_id, post_id)
);

-- 이메일 연락 이력
create table if not exists public.email_contacts (
  id        bigint generated always as identity primary key,
  sender_id uuid not null references public."user"(id) on delete cascade,
  post_id   bigint not null references public.posts(id) on delete cascade,
  sent_at   timestamptz not null default now(),
  constraint email_contacts_sender_id_post_id_key unique (sender_id, post_id)
);

-- 차단
create table if not exists public.blocks (
  id         bigint generated by default as identity primary key,
  blocker_id uuid not null references public."user"(id) on delete cascade,
  blocked_id uuid not null references public."user"(id) on delete cascade,
  created_at timestamptz not null default now(),
  constraint unique_block unique (blocker_id, blocked_id),
  constraint no_self_block check (blocker_id <> blocked_id)
);

-- 신고
create table if not exists public.reports (
  id             bigint generated by default as identity primary key,
  reporter_id    uuid not null references public."user"(id) on delete cascade,
  target_user_id uuid not null references public."user"(id) on delete cascade,
  match_id       bigint references public.matches(id) on delete set null,
  reason         text not null,
  status         text not null default 'pending',
  created_at     timestamptz not null default now(),
  constraint reports_status_check check (status = any (array['pending','reviewed','resolved']))
);

-- ===== 3. 함수 =====
-- 신규 auth 사용자 → public."user" 자동 생성
create or replace function public.handle_new_user()
returns trigger language plpgsql security definer as $$
begin
  insert into public."user" (id, email)
  values (new.id, new.email);
  return new;
end;
$$;

-- updated_at 자동 갱신
create or replace function public.update_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- ===== 4. 트리거 =====
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

drop trigger if exists posts_updated_at on public.posts;
create trigger posts_updated_at
  before update on public.posts
  for each row execute function public.update_updated_at();

drop trigger if exists matches_updated_at on public.matches;
create trigger matches_updated_at
  before update on public.matches
  for each row execute function public.update_updated_at();

